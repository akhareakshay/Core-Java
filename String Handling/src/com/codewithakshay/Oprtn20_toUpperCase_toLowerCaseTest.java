package com.codewithakshay;

public class Oprtn20_toUpperCase_toLowerCaseTest {

	/*
	 * toUpperCase() method converts the given string to upper case
	 * 
	 * toLowerCase() method converts the given string to lower case
	 */

	public static void main(String[] args) {

		String str = "hey coders, welcome to codewithakshay family";
		System.out.println("Original String : " + str);
		System.out.println("Converting given String to upper case : " + str.toUpperCase());

		String str2 = "WHATS APP BUDDIES, LETS CODE HARD";
		System.out.println("Converting given String to lower case : " + str2.toLowerCase());

	}

}
